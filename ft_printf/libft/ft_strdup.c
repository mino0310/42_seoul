#include "libft.h"

char		*ft_strdup(const char *str)
{
	int		i;
	int		len;
	char	*new_str;

	len = ft_strlen(str);
	if (!(new_str = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	i = 0;
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
